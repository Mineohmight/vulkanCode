    void processInputs()
    {
        for (std::pair<int, bool> pair : keyboardLoggedStatesMap) {

            if (pair.second) // pair.second (a boolean) holds whether the key is pressed down or not, if true it is down
            {
                switch (pair.first) // pair.first holds the particular GLFW keyboard key value in the keyboardLoggedStatesMap
                {
                case GLFW_KEY_W: // FORWARD
                    camera.position += camera.speed * camera.front * frameTimeSeconds;
                    break;
                case GLFW_KEY_S: // BACKWARD
                    camera.position -= camera.speed * camera.front * frameTimeSeconds;
                    break;
                case GLFW_KEY_A: // LEFT
                    camera.position -= glm::normalize(glm::cross(camera.front, camera.top)) * camera.speed * frameTimeSeconds;
                    break;
                case GLFW_KEY_D: // RIGHT
                    camera.position += glm::normalize(glm::cross(camera.front, camera.top)) * camera.speed * frameTimeSeconds;
                    break;
                case GLFW_KEY_P: // save scene
                    std::cout << indices.size() << "\n";
                    break;
                }
            }
        }

        for (std::pair<int, bool> pair : mouseLoggedStatesMap) {

            if (pair.second) // pair.second (a boolean) holds whether the button is pressed down or not, if true it is down
            {
                switch (pair.first) // pair.first holds the particular GLFW mouse button value in the mouseLoggedStatesMap
                {
                case GLFW_MOUSE_BUTTON_LEFT:
                    if (onlyLoadModelOnce)
                    {
                        addMeshToScene("threetetrahedramodel.bin");
                    }
                    break;
                case GLFW_MOUSE_BUTTON_RIGHT:
                    break;
                }
            }
        }
    }

    void addMeshToScene(std::string meshFileName)
    {
        {
            std::ifstream fileExistsCheck;
            fileExistsCheck.open(meshFileName);
            if (!fileExistsCheck)
            {
                std::cout << "\n\nERROR: File does not exist.\n\n";
                return;
            }
        }

        unserializeMesh(meshFileName);

        std::vector<uint16_t> indicesToBeAdded = indicesSerialized;

        std::cout << "SIZE: " << indicesSerialized.size() << std::endl;

        if (indicesToBeAdded.size() + indices.size() > MAX_NUMBER_OF_INDICES_LOADED)
        {
            std::cout << "\n\n#########\n\nERROR: Trying to load more points than MAX_NUMBER_OF_POINTS_LOADED\n\n################\n\n";
        }

        // I have no reason to believe this will ever be false, so I could possibly remove this in future implementations
        // ! this needs to be done when the first value equals zero otherwise the indices will not be correct !
        if (indicesToBeAdded.at(0) == 0) 
        {
            for (int i = 0; i < indicesToBeAdded.size(); i++)
            {
                indicesToBeAdded.at(i) += vertices.size();
            }
        }
        else {
            for (int i = 0; i < indicesToBeAdded.size(); i++)
            {
                indicesToBeAdded.at(i) += (vertices.size()-1);
            }
        }

        std::vector<Vertex> tempVerticesUnserialized;

        for (int i = 0; i < verticesSerialized.size(); i++)
        {
            Vertex tempVertexObject;
            tempVertexObject.pos = verticesSerialized.at(i).pos;
            tempVertexObject.color = verticesSerialized.at(i).color;
            tempVerticesUnserialized.push_back(tempVertexObject);
        }


        // concatenate data
        for (int i = 0; i < tempVerticesUnserialized.size(); i++)
        {
            vertices.push_back(tempVerticesUnserialized.at(i));
        }

        for (int i = 0; i < indicesToBeAdded.size(); i++)
        {
            indices.push_back(indicesToBeAdded.at(i));
        }

        for (int i = 0; i < vertices.size(); i++)
        {
            std::cout << "Vertex " << i << " : " << vertices.at(i).pos.x << " " << vertices.at(i).pos.y << " " << vertices.at(i).pos.z << " ";
            std::cout << vertices.at(i).color.x << " " << vertices.at(i).color.y << " " << vertices.at(i).color.z << "\n";
        }

        for (int i = 0; i < indices.size(); i++)
        {
            std::cout << "Index " << i << " : " << indices.at(i) << "\n";
        }

        updateVertexAndIndexBuffers(vertices, indices);

        onlyLoadModelOnce = false;
    }

