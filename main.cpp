void key_callback(GLFWwindow* window, int key, int scancode, int action, int mods)
{
    // key_callback sets flags for key down or key up, then movement logic is calculated elsewhere
    if (action == GLFW_PRESS)
    {
        keyboardLoggedStatesMap[key] = true;
    }
    else if (action == GLFW_RELEASE)
    {
        keyboardLoggedStatesMap[key] = false;
    }
}

/*

You usually do this with double buffering. You keep 2 variables, the "now" value 
and the "old" value. In each tick of the update loop you write the "now" value into 
the "old" value. Then you read the new "now" value from the mouse button callback.
With both the "old" and "now" values you can find out whether the mouse was 
just pressed (old == false, now == true), the mouse was just released (old == true, now == false) 
or whether it has been continously pressed (old == now == true).

I could couple this with a counter that can use this buffer over a range of calls, so I can adjust things.
Something to note though is that this counter could introduce unpredictable behaviour on different machines,
some running faster than others so the timing being off. This counter may not be needed anyway.

*/


// MOUSE BUTTON CALLBACK
void mouse_button_callback(GLFWwindow* window, int button, int action, int mods)
{

    // key_callback sets flags for key down or key up, then movement logic is calculated elsewhere
    if (action == GLFW_PRESS)
    {
        mouseLoggedStatesMap[button] = true;
    }
    else if (action == GLFW_RELEASE)
    {
        mouseLoggedStatesMap[button] = false;
    }
}

// MOUSE CALLBACK
void cursor_position_callback(GLFWwindow* window, double xpos, double ypos) {

    // code from learnopengl.com > Getting Started > Camera

    if (firstMouse)
    {
        camera.cursor.lastX = xpos;
        camera.cursor.lastY = ypos;
        firstMouse = false;
    }

    camera.cursor.xoffset = (xpos - camera.cursor.lastX) * camera.cursor.sensitivity;
    camera.cursor.yoffset = (camera.cursor.lastY - ypos) * camera.cursor.sensitivity;
    camera.cursor.lastX = xpos;
    camera.cursor.lastY = ypos;

    camera.cursor.yaw += camera.cursor.xoffset;
    camera.cursor.pitch += camera.cursor.yoffset;

    if (camera.cursor.pitch > 89.0f)
        camera.cursor.pitch = 89.0f;
    if (camera.cursor.pitch < -89.0f)
        camera.cursor.pitch = -89.0f;

    camera.front = glm::normalize(glm::vec3(
        cos(glm::radians(camera.cursor.yaw)) * cos(glm::radians(camera.cursor.pitch)),
        sin(glm::radians(camera.cursor.pitch)),
        sin(glm::radians(camera.cursor.yaw)) * cos(glm::radians(camera.cursor.pitch))
    ));
}

